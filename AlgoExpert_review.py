# #  Valid Subsequence
def isValidSubsequence(a, b):
  if len(b) > len(a):
    return False
  a_pos = 0  
  for i in range(len(b)):
    if a_pos == len(a)-1:
      return False
    else:
      for j in range(a_pos, len(a)):
        if a[j] == b[i]:
          a_pos = j + 1
          break
        else:
          a_pos = j
  return True        

